"""Core computation functions for the modular project example."""


def compute(x: float, y: float) -> float:
    """Compute the sum of two numbers.

    Parameters
    ----------
    x : float
        First number.
    y : float
        Second number.

    Returns
    -------
    result : float
        Sum of ``x`` and ``y``.

    Examples
    --------
    >>> compute(1.0, 2.0)
    3.0
    """
    return x + y
